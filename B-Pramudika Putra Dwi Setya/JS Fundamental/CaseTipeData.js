function showDataTypes() {
    let name = "Pramudika Putra Dwi Setya"; // String
    let age = 21; // Number
    let isStudent = true; // Boolean
    let courses = ["Math", "Science", "History"]; // Array
    let person = { firstName: "Pramudika", lastName: "Putra Dwi Setya" }; // Object
    let undefinedVar; // Undefined
    let nullVar = null; // Null

    console.log("Name (String):", name);
    console.log("Age (Number):", age);
    console.log("Is Student (Boolean):", isStudent);
    console.log("Courses (Array):", courses);
    console.log("Person (Object):", person);
    console.log("Undefined Variable:", undefinedVar);
    console.log("Null Variable:", nullVar);

    alert("Check the console to see different data types in JavaScript.");
}
